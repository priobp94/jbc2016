          <b>Debat marketing di JBC 2017 apakah sama dengan tahun lalu ?</b><br>
Secara regulasi tidak ada perubahan berarti untuk debat marketing tahun ini. Mungkin perbedaannya adalah cakupan dari mosi yang diperdebatkan karena menyesuaikan juga dengan tema yang selalu berubah tiap tahunnya.<br><br>

<b>Debat marketing di JBC 2017 nanti tahapannya apa saja ?</b><br>
Nanti akan ada beberapa tahapan yang kita sebut sebagai babak. Babak pertama adalah preliminary atau babak penyisihan. Dimana akan diambil 8 tim terbaik yang akan lolos ke babak selanjutnya, yaitu quarter final. Dari babak ini akan diperoleh 4 tim yang selanjutnya akan bertanding di babak Semifinal. Akhirnya, akan diperoleh 2 tim untuk memperebutkan juara 3 serta 2 tim yang akan memperebutkan juara 1.<br><br>

<b>Bagaimana cara atau sistem penilaiannya nanti ?</b><br>
Sistem penilaian akan menggunakan victory point dan score. Untuk keterangan dan penjelasan lebih lanjut dapat mendownload guide book<br><br>

<b>Untuk peraturan debatnya sendiri seperti apa ?</b><br>
Peraturan sendiri mungkin hampir sama dengan peraturan di lomba debat pada umumnya. Tetapi untuk lebih jelas, peraturan yang lebih lengkap dapat mendownload  guide book.<br><br>
