          <b>Simbis JBC 2016 itu kompetisi apa sih Kak?</b><br>
Simulasi Bisnis Java Business Competition 2017 adalah kompetisi game simulasi bisnis JA Titan dan Studi Kasus sesuai tema “Improving Technopreneurship Excitement In Achieve Sustainable Development”.<br><br>

<b>Adakah batasan jenjang pendidikan yang dapat mengikuti kompetisi Simbis?</b><br>
Kami membatasi jenjang pendidikan kompetisi Simbis hanya untuk mahasiswa D3/S1 se-Indonesia.<br><br>

<b>Tahapan/ bentuk perlombaannya apa saja, Kak?</b><br>
Ada 3 tahap, yaitu Seleksi Online, Playoff, dan Final. Dalam Seleksi Online, peserta bermain JA Titan secara online di tempat masing-masing. Dalam Playoff terdapat 2 playoff dan semifinal bermain JA Titan di kampus Telkom University. Dalam Grandfinal kemudian akan ada Case Study mengenai tema.<br><br>

<b>Berapa tim yang lolos ke Playoff?</b><br>
20 tim terbaik berdasarkan PI JA Titan dalam Seleksi Online.<br><br>

<b>Berapa tim yang lolos ke Grandfinal?</b><br>
4 tim terbaik berdasarkan PI JA Titan dalam Playoff.<br><br>

<b>Adakah ketentuan banyaknya anggota dalam 1 tim?</b><br>
Ya, setiap tim maksimal terdiri dari 2 orang anggota.<br><br>

<b>Apakah boleh 1 tim terdiri dari anggota yang berbeda universitas?</b><br>
Mohon maaf, 1 tim hanya boleh terdiri dari anggota yang berasal dari universitas yang sama walaupun beda prodi beda fakultas.<br><br>


<b>Apakah ada ketentuan dalam penggunaan nama tim?</b><br>
Penggunaan nama tim bebas tetapi harus sopan dan tidak mengandung hinaan, umpatan, provokasi maupun SARA juga tidak menggunakan nama universitas. Panitia memiliki hak sepenuhnya untuk menentukan suatu tim lolos persyaratan atau tidak.<br><br>

<b>Berapakah besarnya biaya pendaftaran?</b><br>
Biaya pendaftaran sebesar Rp 100.000 per tim (Early Bird), Rp 150.000 per tim (Reguler), Rp 200.000 per tim (Lolos Seleksi).<br><br>

