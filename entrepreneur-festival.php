          <b>Kak E-fest itu kompetisi apa sih?</b><br>
E-fest atau Entrepreneur Festival merupakan cabang lomba yang diperuntukan untuk mahasiswa/i yang telah melakukan Start-up bisnis dibidang teknologi maupun yang memiliki ide bisnis di bidang teknologi.<br><br>

<b>Tema E-fest tahun ini apa?</b><br>
Pada tahun ini kita akan mengangkat tema "<i>Technopreneur Roles to Empower Competitive Spirit in Global Competition</i>".<br><br>

<b>Apa yang dimaksud dengan Technopreneur itu sendiri kak, apa saja persyaratannya?</b><br>
Technopreneur merupakan pelaku bisnis di bidang teknologi baik software maupun hardware. Persyaratan pada kompetisi E-Fest JBC 2017 ini adalah seluruh mahasiswa di Indonesia yang memiliki start up business atau ide bisnis berbasis inovasi teknologi baik dalam bentuk digital (software) maupun hardware.<br><br>

<b>Siapa saja sih kak yang bisa mengikuti lomba?</b><br>
Seluruh Mahasiswa/i yang terdaftar di universitas yang ada di Indonesia.<br><br>

<b>Bagaimana cara mendaftarnya kak?</b><br>
  <ol>
    <li>Siapkan Bisnis Model Canvas kamu</li>
    <li>Buka Website</li>
    <li>Lalu, isi formulir pendaftaran</li>
    <li>Selesaikan transaksi pembayaran</li>
    <li>Kini kamu sudah terdaftar dalam lomba E-fest</li>
  </ol>
  <br>

<b>Berapa tahap untuk sampai kebabak final kak?</b><br>
ada 3 tahap yaitu Seleksi Online, Semifinal, dan Grandfinal. Seleksi Online hanya pengumpulan Bisnis Model Canvas saja, sedangkan Semiffinal dan Grandfinal dilakukan dihari yang sama di Bandung.<br><br>

<b>Kak 1 tim terdiri dari berapa anggota?</b><br>
1 tim terdiri dari 2 anggota.<br><br>

<b>Kak apakah 1 tim boleh hanya terdiri dari 1 anggota?</b><br>
Ya boleh, selama tidak lebih dari 2 anggota.<br><br>


<b>Apakah ada ketentuan dengan nama tim?</b><br>
Tidak ada, hanya saja harus sopan dan tidak mengandung hinaan, umpatan, provokasi maupun SARA.<br><br>

<b>Kak apakah dalam satu Tim boleh berebeda Universitas?</b><br>
Tidak bisa, dalam 1 tim hanya boleh berasal dari Universitas yang sama.<br><br>

<b>Berapa tim kak yang akan lolos Semifinal?</b><br>
14 Tim yang akan lolos final.<br><br>

<b>Berapa tim kak yang akan lolos Grandfinal?</b><br>
3 Tim yang akan lolos final.<br><br>

<b>Jika saya tidak dapat hadir pada saat Semifinal maupun Grandfinal, apakah saya bisa menyuruh orang lain menggantikan saya?</b><br>
Peserta lomba tidak dapat digantikan oleh siapapun.<br><br>

